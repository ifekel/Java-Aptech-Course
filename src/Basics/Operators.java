package Basics;

public class Operators {
    public static void main(String[] args) {
        int x = 23;
        int y = 12;

        System.out.print("X & y: ");
        System.out.println(x & y);

        System.out.print("x | y: ");
        System.out.println(x | y);

        System.out.print("x ^ y: ");
        System.out.println(x ^ y);
    }
}
